class Sensor:

    def __init__(self, sensor_id, sensor_type, value):
        self.sensor_id = sensor_id
        self.sensor_type = sensor_type
        self.value = value

    def __str__(self):
        return f"Sensor {self.sensor_id}: {self.value} ({self.sensor_type})"

    def __repr__(self):
        return f"Sensor(sensor_id={self.sensor_id!r}, sensor_type={self.sensor_type!r}, value={self.value!r})"


s1 = Sensor("S001", "Temperature", 72.5)
s2 = Sensor("S002", "Pressure", 101.3)

print(s1)           # calls __str__
print(repr(s1))     # calls __repr__

sensors = [s1, s2]
print(sensors)      # lists call __repr__ on each item  


