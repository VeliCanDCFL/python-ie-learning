from dataclasses import dataclass, field


@dataclass
class WorkOrder:
    order_id: str
    product: str
    quantity: int
    priority: str = "normal"        # default value
    completed: bool = False         # default value

    def complete(self):
        self.completed = True
        print(f"Order {self.order_id} completed")

    def __str__(self):
        status = "✓" if self.completed else "pending"
        return f"[{status}] {self.order_id} | {self.product} x{self.quantity} | {self.priority}"


w1 = WorkOrder("WO001", "Steel Bolt", 500)
w2 = WorkOrder("WO002", "Aluminum Plate", 200, priority="high")
w3 = WorkOrder("WO003", "Titanium Rod", 50, priority="urgent")

print(w1)
print(w2)
print(w3)

print(repr(w1))     # __repr__ is auto-generated by dataclass

w2.complete()
print(w2)